'''
Date : 18th April 2024
Desc : Print days of week 
'''

day = int(input('Enter the number from 1 to 7 for day of week : '))

match (day) :
    case 1 :
        print('1. Monday')
    case 2 :
        print('2. Tuesday')
    case 3 :
        print('3. Wednesday')
    case 4 :
        print('4. Thursday')
    case 5 :
        print('5. Friday')
    case 6 :
        print('6. Saturday')
    case 7 :
        print('7. Sunday')
    case _ :
        print('Invalid input')







    
